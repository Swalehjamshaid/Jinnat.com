async def audit_event_generator(url: str) -> AsyncGenerator[str, None]:
    """
    Streams audit progress to frontend.
    Runs run_audit() in background threads to avoid blocking.
    """

    try:
        # Initial message
        yield sse_format({
            "crawl_progress": 10,
            "status": "Starting engineâ€¦",
            "finished": False,
        })
        await asyncio.sleep(0.3)

        yield sse_format({
            "crawl_progress": 30,
            "status": "Checking network & SSLâ€¦",
            "finished": False,
        })
        await asyncio.sleep(0.4)

        # Run our main audit pipeline (crawler + SEO + performance + charts)
        audit_result = await asyncio.to_thread(run_audit, url)

        yield sse_format({
            "crawl_progress": 80,
            "status": "Building charts & insightsâ€¦",
            "finished": False,
        })
        await asyncio.sleep(0.3)

        # Final SSE payload sent to index.html
        final_payload = {
            **audit_result,
            "finished": True,
            "crawl_progress": 100,
            "status": "Audit Complete âœ”"
        }

        yield sse_format(final_payload)

    except Exception as e:
        logger.exception("Audit failed for %s", url)
        yield sse_format({
            "finished": True,
            "error": str(e),
            "crawl_progress": 100,
            "status": "Audit failed."
        })


